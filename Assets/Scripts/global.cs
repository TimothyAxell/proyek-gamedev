public class global
{
    string judul;
    string deskripsi;

    public global(string paramJudul, string paramDeskirpsi)
    {
        judul = paramJudul;
        deskripsi = paramDeskirpsi;
    }

    public string getJudul()
    {
        return judul;
    }

    public string getDeskripsi()
    {
        return deskripsi;
    }




}
